//saves a board as an array of points for the Creator (me), not the user
function save() {
  var liveCells = [];
  twoDLoop(board, function (cell) {
    if (cell.status) {
      liveCells.push( [cell.column, cell.row] )
      console.log([cell.column, cell.row]);
    }
  })
  return liveCells
}

